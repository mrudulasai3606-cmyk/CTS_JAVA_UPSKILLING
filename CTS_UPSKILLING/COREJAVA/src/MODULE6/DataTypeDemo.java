package MODULE6;


public class DataTypeDemo {
    public static void main(String[] args) {

        // Primitive Data Types
        int age = 21;
        float height = 5.9f;
        double salary = 50000.75;
        char grade = 'A';
        boolean isJavaFun = true;

        // Display values
        System.out.println("Integer Value (int): " + age);
        System.out.println("Float Value (float): " + height);
        System.out.println("Double Value (double): " + salary);
        System.out.println("Character Value (char): " + grade);
        System.out.println("Boolean Value (boolean): " + isJavaFun);
    }
}